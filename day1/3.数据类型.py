'''
数据类型:type(数据)来监测数据类型
'''

# <class 'int'> 整型
num = 1

# <class 'float'> 浮点型
num2 = 1.1

# <class 'str'> 字符串
a = "holle"

# <class 'bool'> 布尔型
b = True

# <class 'list'> 列表
c = [1, 2, 3]

# <class 'tuple'> 元祖
d = (1, 2, 3)

# <class 'set'> 集合
e = {1, 2, 3}

# <class 'dict'> 字典,键值对
f = {"name": "chryl", "age": 15}

print(type(num))
print(type(num2))
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
